package mathpack;
public class Trigo
{
	public double sin(double p,double h)
	{
		return p/h; 
	}
	public double cos(double b,double h)
	{
		return b/h;
	}
	public double tan(double p,double b)
	{
		return p/b;
	}
	public double cosec(double p,double h)
	{
		return h/p;
	}
	public double sec(double b,double h)
	{
		return h/b;
	}
	public double cot(double p,double b)
	{
		return b/p;
	}
}
